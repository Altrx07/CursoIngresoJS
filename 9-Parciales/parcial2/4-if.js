//Debemos lograr mostrar un mensaje al presionar el botón  'MOSTRAR'.
function Mostrar()
{
	var num1 = prompt ("Ingrese el primer número");
	var num2 = prompt ("Ingrese el seundo número");
	var multiplicar;
	var sumar;
	var restar;
	var resultado1;
	var resultado2;
	var resultado3;

	num1 = parseInt (num1);

	num2 = parseInt (num2);

	if (num1 = num2)
	{
		multiplicar = num1 * num2;

		resultado1 = multiplicar;

		document.write = resultado1;

		alert("El resultado es"+ resultado1);
		break;
	}
	else (num1 > num2);
	{
	 restar = num1 - num2;

	 resultado2 = restar;

	 document.write = resultado2;

	 alert("El resultado es"+ resultado2);
	 break;
	}
	if (num1 < num2);
	{
		sumar = num2 + num1;

		resultado3 = sumar;

		document.write = resultado3;

		alert("El resultado es"+ resultado3);
		
    }

	}




